#etapa de la vida, según edad(EVE422)

etapaVida= int(input("digite la etapa de la vida, según la edad: "))
if(etapaVida >=0 and etapaVida <14):
    print("Niño")
elif(etapaVida >=15 and etapaVida <28):
    print("Joven")
elif(etapaVida >=28 and etapaVida <60):
    print("Adulto") 
else:
    print("Adulto mayor")     








  
